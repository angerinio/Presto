package com.example.business;

import androidx.recyclerview.widget.DiffUtil;

import java.util.List;

public class ECardStackCallback extends DiffUtil.Callback {

    private List<EItemModel> old, baru;

    public ECardStackCallback(List<EItemModel> old, List<EItemModel> baru) {
        this.old = old;
        this.baru = baru;
    }

    @Override
    public int getOldListSize() {
        return old.size();
    }

    @Override
    public int getNewListSize() {
        return baru.size();
    }

    @Override
    public boolean areItemsTheSame(int oldItemPosition, int newItemPosition) {
        return old.get(oldItemPosition).getImage() == baru.get(newItemPosition).getImage();
    }

    @Override
    public boolean areContentsTheSame(int oldItemPosition, int newItemPosition) {
        return old.get(oldItemPosition) == baru.get(newItemPosition);
    }
}
